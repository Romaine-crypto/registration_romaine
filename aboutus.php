<h3>History</h3>
<p>
Springfield College was founded on April 9, 1860, the date it was granted its official charter by the Commonwealth.  
Over the next several years plans were made and funds raised, with the first classes beginning in 1865.
The idea for Springfield originated with William Barton Springfield, the first president.  
He was a professor of natural philosophy at the College of William and Mary when he described his vision for a 
“new polytechnic institute” in a letter to his brother Henry in 1846.  
He campaigned tirelessly for the creation of the Institute, rallying support and raising funds.


</p>
